import csv
import subprocess
import sys
from argparse import ArgumentParser
from pathlib import Path


PROJECT_ROOT = Path(__file__).resolve().parent
STABILIZE_SCRIPT = PROJECT_ROOT / "stabilize.py"


def parse_args():
    parser = ArgumentParser()
    parser.add_argument(
        "--degradations_csv",
        required=True,
        help="CSV generated by generate_degradations.py",
    )
    parser.add_argument(
        "--output_dir",
        required=True,
        help="directory where stabilized outputs and the benchmark summary will be written",
    )
    parser.add_argument(
        "--motion_backend",
        default="lk",
        choices=["lk", "raft"],
        help="motion-estimation backend used for all benchmark runs",
    )
    parser.add_argument("--model", help="RAFT checkpoint path; required when --motion_backend=raft")
    parser.add_argument(
        "--smoothing",
        default="moving_average",
        choices=["moving_average", "ema"],
        help="trajectory smoother passed to stabilize.py",
    )
    parser.add_argument("--smooth_radius", type=int, default=30, help="smoothing radius passed to stabilize.py")
    parser.add_argument(
        "--correction_strength",
        type=float,
        default=1.0,
        help="correction strength passed to stabilize.py",
    )
    parser.add_argument("--crop_margin", type=int, default=20, help="crop margin passed to stabilize.py")
    parser.add_argument(
        "--border_mode",
        choices=["constant", "reflect", "replicate"],
        default="reflect",
        help="warp border mode passed to stabilize.py",
    )
    parser.add_argument("--max_frames", type=int, help="optional cap on processed frames")
    parser.add_argument(
        "--skip_output_evaluation",
        action="store_true",
        help="skip residual-motion evaluation for faster runs",
    )
    parser.add_argument(
        "--overwrite",
        action="store_true",
        help="rerun even if the expected metrics CSV already exists",
    )
    parser.add_argument(
        "--stabilize_quiet",
        action="store_true",
        help="pass --quiet to stabilize.py for less terminal output",
    )
    args = parser.parse_args()

    if args.motion_backend == "raft" and not args.model:
        parser.error("--model is required when --motion_backend=raft")
    if args.smooth_radius < 0:
        parser.error("--smooth_radius must be non-negative")
    if not 0.0 <= args.correction_strength <= 1.0:
        parser.error("--correction_strength must be in the range [0, 1]")
    if args.crop_margin < 0:
        parser.error("--crop_margin must be non-negative")
    return args


def load_csv_rows(csv_path):
    with csv_path.open("r", newline="", encoding="utf-8") as handle:
        return list(csv.DictReader(handle))


def load_metrics(metrics_path):
    metrics = {}
    with metrics_path.open("r", newline="", encoding="utf-8") as handle:
        reader = csv.DictReader(handle)
        for row in reader:
            metrics[row["metric"]] = row["value"]
    return metrics


def build_command(args, input_video, output_video):
    command = [
        sys.executable,
        str(STABILIZE_SCRIPT),
        "--motion_backend",
        args.motion_backend,
        "--video",
        str(input_video),
        "--output",
        str(output_video),
        "--smoothing",
        args.smoothing,
        "--smooth_radius",
        str(args.smooth_radius),
        "--correction_strength",
        str(args.correction_strength),
        "--crop_margin",
        str(args.crop_margin),
        "--border_mode",
        args.border_mode,
    ]

    if args.motion_backend == "raft":
        command.extend(["--model", args.model])
    if args.max_frames is not None:
        command.extend(["--max_frames", str(args.max_frames)])
    if args.skip_output_evaluation:
        command.append("--skip_output_evaluation")
    if args.stabilize_quiet:
        command.append("--quiet")
    return command


def write_summary(summary_path, rows):
    if not rows:
        return

    preferred_keys = [
        "variant_name",
        "degradation_type",
        "parameter",
        "input_video",
        "output_video",
        "motion_backend",
        "smoothing",
        "smooth_radius",
        "correction_strength",
        "frames_processed",
        "avg_motion_estimation_ms",
        "motion_estimation_fps",
        "output_to_input_std_ratio_x",
        "output_to_input_std_ratio_y",
        "output_to_input_mean_abs_ratio_x",
        "output_to_input_mean_abs_ratio_y",
        "valid_fov_ratio_after_crop",
    ]
    dynamic_keys = []
    for row in rows:
        for key in row.keys():
            if key not in preferred_keys and key not in dynamic_keys:
                dynamic_keys.append(key)

    fieldnames = preferred_keys + dynamic_keys
    with summary_path.open("w", newline="", encoding="utf-8") as handle:
        writer = csv.DictWriter(handle, fieldnames=fieldnames)
        writer.writeheader()
        writer.writerows(rows)


def main():
    args = parse_args()
    degradations_csv = Path(args.degradations_csv)
    if not degradations_csv.exists():
        raise FileNotFoundError(f"Degradation CSV does not exist: {degradations_csv}")

    rows = load_csv_rows(degradations_csv)
    output_dir = Path(args.output_dir)
    output_dir.mkdir(parents=True, exist_ok=True)
    summary_rows = []

    print(f"Running {len(rows)} degradation benchmark(s)")
    for index, row in enumerate(rows, start=1):
        input_video = Path(row["output_video"])
        run_name = f"{row['variant_name']}_{args.motion_backend}_{args.smoothing}_r{args.smooth_radius}_cs{str(args.correction_strength).replace('.', 'p')}"
        output_video = output_dir / f"{run_name}.mp4"
        metrics_path = output_dir / f"{run_name}_metrics.csv"

        print(f"[{index}/{len(rows)}] {row['variant_name']}")
        if metrics_path.exists() and not args.overwrite:
            print(f"  skipping existing run, reusing {metrics_path.name}")
            metrics = load_metrics(metrics_path)
        else:
            command = build_command(args, input_video, output_video)
            print("  " + " ".join(command))
            subprocess.run(command, check=True, cwd=PROJECT_ROOT)
            if not metrics_path.exists():
                raise FileNotFoundError(f"Expected metrics CSV was not created: {metrics_path}")
            metrics = load_metrics(metrics_path)

        summary_row = {
            "variant_name": row["variant_name"],
            "degradation_type": row["degradation_type"],
            "parameter": row["parameter"],
            "input_video": str(input_video),
            "output_video": str(output_video),
            "motion_backend": args.motion_backend,
            "smoothing": args.smoothing,
            "smooth_radius": args.smooth_radius,
            "correction_strength": args.correction_strength,
        }
        summary_row.update(metrics)
        summary_rows.append(summary_row)

    summary_path = output_dir / "degradation_benchmark_summary.csv"
    write_summary(summary_path, summary_rows)
    print(f"Saved benchmark summary CSV to: {summary_path}")


if __name__ == "__main__":
    main()
